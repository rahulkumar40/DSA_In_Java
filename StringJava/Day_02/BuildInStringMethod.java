package Day_02;

public class BuildInStringMethod {
    public static void main(String[] args) {
        String s = "Rahul Kumar";

        // to find any character at any index :: charAr(index)
        System.out.println(s.charAt(8));

        // total lenght of string :: length()
        int n = s.length();

        System.out.print("Total length : " + n);
    }
}
